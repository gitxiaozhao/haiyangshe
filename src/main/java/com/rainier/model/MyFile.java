package com.rainier.model;

public class MyFile {
    private Integer id;
    private String name;
    private String bendiUrl;
    private String fuwuqiUrl;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBendiUrl() {
        return bendiUrl;
    }

    public void setBendiUrl(String bendiUrl) {
        this.bendiUrl = bendiUrl;
    }

    public String getFuwuqiUrl() {
        return fuwuqiUrl;
    }

    public void setFuwuqiUrl(String fuwuqiUrl) {
        this.fuwuqiUrl = fuwuqiUrl;
    }
}
